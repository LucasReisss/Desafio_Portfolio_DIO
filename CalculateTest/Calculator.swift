import Foundation

class Calulator {
    func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    
    func sutract(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
    
    func multiply(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
    
    func divide(_ a: Int, _ b: Int) -> Int {
        return a / b
    }
    
}
